package kr.co.beans;


public class AddressBean {

	private String address_id;
	private String code;
	private String address_name;
	private String member_name;
	private String member_phone;
	private String member_post;
	private String member_add;
	private String member_addt;

	public String getAddress_id() {
		return address_id;
	}

	public void setAddress_id(String address_id) {
		this.address_id = address_id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getAddress_name() {
		return address_name;
	}

	public void setAddress_name(String address_name) {
		this.address_name = address_name;
	}

	public String getMember_name() {
		return member_name;
	}

	public void setMember_name(String member_name) {
		this.member_name = member_name;
	}

	public String getMember_phone() {
		return member_phone;
	}

	public void setMember_phone(String member_phone) {
		this.member_phone = member_phone;
	}

	public String getMember_post() {
		return member_post;
	}

	public void setMember_post(String member_post) {
		this.member_post = member_post;
	}

	public String getMember_add() {
		return member_add;
	}

	public void setMember_add(String member_add) {
		this.member_add = member_add;
	}

	public String getMember_addt() {
		return member_addt;
	}

	public void setMember_addt(String member_addt) {
		this.member_addt = member_addt;
	}

}